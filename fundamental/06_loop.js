// for loop
for (let i = 0; i < 5; i++) {
     console.log(i);
}

// for of
let myArray = ["luke", "Han", "Chewbacca", "Leila"];

for (const arrayItem of myArray) {
     console.log(arrayItem);
}

// while
let j = 1;
while (j <= 100) {
     console.log(j);
     j++;
}

// do..while
let c = 1;
do {
     console.log(c);
     c++;
} while (c < 10);
